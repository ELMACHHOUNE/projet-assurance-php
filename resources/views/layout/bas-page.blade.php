      <p class="contenuindex"><span class="contenuindex1"><strong>1.Qu’est-ce que le PER </strong></span>
      <p class="contenuindex">
        Le PER est un produit d’épargne à long terme lancé en 2019 pour préparer sa
        retraite. Il remplace les anciens dispositifs comme le PERP ou Madelin. L’argent est
        généralement bloqué jusqu’à la retraite (sauf exceptions).
      </p>


      <span class="contenuindex1"><strong>2.Les 3 types de PER:</strong></span>


      <ul>
        <li><strong>PER individuel (PERI) : </strong>ouvert par tout particulier.</li>
        <li><strong>PER d’entreprise obligatoire :</strong>mis en place par l’employeur. </li>
        <li><strong>PER collectif (PERCOL) :</strong> accessible via l’entreprise, mais facultatif.</li>

      </ul>



